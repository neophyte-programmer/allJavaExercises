
public class application {

	public static void main(String[] args) {
		int value = 0;
		
		while (value <= 10) //condition for the loop to run
		{
			//actual loop
			System.out.println("The value of value at this iteration is: " + value);
			value++; //code to make sure the loop terminates at some point
		}
		
	}

}
